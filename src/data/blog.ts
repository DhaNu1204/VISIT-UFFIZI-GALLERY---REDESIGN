export function getLocalizedBlogPosts(locale: string): BlogPost[] {
  if (locale === "en") return blogPosts;
  const trans = blogTransMap[locale] || {};
  return blogPosts.map((p) => {
    const t = trans[p.slug];
    return t ? { ...p, ...t } : p;
  });
}

